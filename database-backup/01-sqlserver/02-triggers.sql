-- 3. NODE-MENU SYNCHRONIZATION TRIGGER (AFTER INSERT, UPDATE ON ACC_NODOS)

CREATE TRIGGER TRG_ACC_NODOS_SYNC_MENU
ON ACC_NODOS
AFTER INSERT, UPDATE
AS
BEGIN
    SET NOCOUNT ON;
    
    BEGIN TRY
        -- Insert new nodes into menu nodes if they are visual ecosystem types
        INSERT INTO ACC_MENU_NODOS (
            ID_NODO, ETIQUETA_VISIBLE, URL_RUTA_VISIBLE, SLUG_VISIBLE,
            ICONO_VISIBLE, DESCRIPCION_VISIBLE, ORDEN_VISUAL, ACTIVO, CREADO_POR, FECHA_CREACION
        )
        SELECT 
            i.ID_NODO, i.ETIQUETA, i.URL_RUTA, i.SLUG,
            i.ICONO, i.DESCRIPCION, i.ORDEN_VISUAL, i.ACTIVO, i.CREADO_POR, i.FECHA_CREACION
        FROM inserted i
        INNER JOIN ACC_TIPOS_NODO t ON i.ID_TIPO_NODO = t.ID_TIPO_NODO
        WHERE t.CODIGO_TIPO IN ('OBJ_ATTR', 'OBJETO', 'OA', 'CARPETA')
          AND NOT EXISTS (SELECT 1 FROM ACC_MENU_NODOS m WHERE m.ID_NODO = i.ID_NODO);

        -- Update existing menu nodes
        UPDATE m
        SET m.ETIQUETA_VISIBLE = i.ETIQUETA,
            m.URL_RUTA_VISIBLE = i.URL_RUTA,
            m.SLUG_VISIBLE = i.SLUG,
            m.ICONO_VISIBLE = i.ICONO,
            m.DESCRIPCION_VISIBLE = i.DESCRIPCION,
            m.ORDEN_VISUAL = i.ORDEN_VISUAL,
            m.ACTIVO = i.ACTIVO,
            m.MODIFICADO_POR = i.MODIFICADO_POR,
            m.FECHA_MODIFICACION = i.FECHA_MODIFICACION
        FROM ACC_MENU_NODOS m
        INNER JOIN inserted i ON m.ID_NODO = i.ID_NODO
        INNER JOIN ACC_TIPOS_NODO t ON i.ID_TIPO_NODO = t.ID_TIPO_NODO
        WHERE t.CODIGO_TIPO IN ('OBJ_ATTR', 'OBJETO', 'OA', 'CARPETA');

    END TRY
    BEGIN CATCH
        -- Insert error log in SQL Server
        INSERT INTO ACC_LOG_ERRORES (MODULO, MENSAJE, FECHA)
        VALUES ('TRIG_SYNC_MENU', 'Error al sincronizar nodo: ' + ERROR_MESSAGE(), GETDATE());
        THROW;
    END CATCH
END;
GO
