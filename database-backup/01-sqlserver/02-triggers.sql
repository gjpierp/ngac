-- 1. AUDITING TRIGGERS (AFTER UPDATE)
-- Using IF UPDATE(...) to prevent recursion loops.

CREATE TRIGGER TRG_ACC_NODOS_AUDIT
ON ACC_NODOS
AFTER UPDATE
AS
BEGIN
    SET NOCOUNT ON;
    IF UPDATE(FECHA_MODIFICACION) OR UPDATE(MODIFICADO_POR) RETURN;
    
    UPDATE ACC_NODOS
    SET FECHA_MODIFICACION = GETDATE(),
        MODIFICADO_POR = SYSTEM_USER
    FROM ACC_NODOS t
    INNER JOIN inserted i ON t.ID_NODO = i.ID_NODO;
END;
GO

