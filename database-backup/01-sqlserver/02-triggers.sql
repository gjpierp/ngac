-- 2. SOFT DELETE TRIGGERS (AFTER UPDATE ON ACTIVO COLUMN)

CREATE TRIGGER TRG_SAFI_ENTIDADES_SD
ON SAFI_ENTIDADES
AFTER UPDATE
AS
BEGIN
    SET NOCOUNT ON;
    IF UPDATE(FECHA_ELIMINACION) OR UPDATE(ELIMINADO_POR) RETURN;
    
    IF UPDATE(ACTIVO)
    BEGIN
        UPDATE SAFI_ENTIDADES
        SET FECHA_ELIMINACION = CASE WHEN i.ACTIVO = 'N' AND d.ACTIVO = 'S' THEN GETDATE()
                                     WHEN i.ACTIVO = 'S' AND d.ACTIVO = 'N' THEN NULL
                                     ELSE t.FECHA_ELIMINACION END,
            ELIMINADO_POR = CASE WHEN i.ACTIVO = 'N' AND d.ACTIVO = 'S' THEN SYSTEM_USER
                                 WHEN i.ACTIVO = 'S' AND d.ACTIVO = 'N' THEN NULL
                                 ELSE t.ELIMINADO_POR END
        FROM SAFI_ENTIDADES t
        INNER JOIN inserted i ON t.ID_ENTIDAD = i.ID_ENTIDAD
        INNER JOIN deleted d ON t.ID_ENTIDAD = d.ID_ENTIDAD;
    END
END;
GO

